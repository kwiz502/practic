#1. Даны числа x, у. Проверить истинность высказывания: «Точка с координатами (x, у)
#лежит в первой или третьей координатной четверти».

x = float(input("Введите координату x: "))
y = float(input("Введите координату y: "))

# Проверка нахождения в первой или третьей координатной четверти
# Первая четверть: x > 0 и y > 0
# Третья четверть: x < 0 и y < 0
i = (x > 0 and y > 0) or (x < 0 and y < 0)

print(f"Точка с координатами ({x}, {y}) лежит в первой или третьей координатной четверти: {i}")